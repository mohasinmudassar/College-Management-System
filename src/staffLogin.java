    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int c=0;
        for(int i=0;i<this.staffMembers.size();i++){
//            System.out.println(staffMembers.size());
            if(this.staffUserName.getText().equals(this.staffMembers.get(i).getName())){
                c++;
                if(this.staffPassword.getText().equals(this.staffMembers.get(i).getPassword())){
                    JOptionPane.showMessageDialog(this,"Login Successfull!");
                    staffFrame sf = new staffFrame();
                    sf.setStaff(this.staffMembers.get(i));
                    this.dispose();
                    sf.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this,"Invalid Password!");
                    clear();
                }
            }
        }
        if(c==0){
            JOptionPane.showMessageDialog(this,"Invalid Username and Password!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = JOptionPane.showInputDialog("Enter name: ");
        String pass = JOptionPane.showInputDialog("Enter Password: ");
        int c=0;
        int age =0;
        try{
            age = Integer.parseInt(JOptionPane.showInputDialog("Enter Age: "));
        }catch(Exception e){
            c++;
        }
        if(name.equals("") || pass.equals("")){
            c++;
        }
        if(c==0){
            staff newStaff = new staff();
            newStaff.setName(name);
            newStaff.setPassword(pass);
            newStaff.setAge(age);
            this.staffMembers.add(newStaff);
            this.writeStaffData();
            this.readStaffData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Invalid Entries!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


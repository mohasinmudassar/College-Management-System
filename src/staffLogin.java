    void readStaffData(){
        try  {  
            //the file to be opened for reading
            FileInputStream fis=new FileInputStream("staffMembers.txt");       
            Scanner sc=new Scanner(fis);  
            while(sc.hasNextLine()){  
                  String data = sc.nextLine();
                  String[] adminTemp = data.split(",");
                  staff s = new staff();
                  s.setName(adminTemp[0]);
                  s.setAge(Integer.parseInt(adminTemp[1]));
                  s.setPassword(adminTemp[2]);
                  staffMembers.add(s);     
            }  
            sc.close();     
            }catch(IOException e){  
                e.printStackTrace();  
            }
    }
    void writeStaffData(){
        try{
            FileWriter myWriter = new FileWriter("staffMembers.txt");
//            Writing all the data inside the staffMembers Arryalist to the file line by line
            for(int i=0;i<staffMembers.size();i++){
                myWriter.write(staffMembers.get(i).getName()+"," + staffMembers.get(i).getAge() + "," + staffMembers.get(i).getPassword() + "\n");
            }
            myWriter.close();
            //System.out.println("Successfully wrote to the file.");
        }catch (IOException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }

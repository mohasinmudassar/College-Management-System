    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        meetingTeacher = JOptionPane.showInputDialog(this,"Enter Teacher Name for meeting: ");
        meetingStudent= JOptionPane.showInputDialog(this,"Enter Student Name for meeting: ");
        meetingParent = JOptionPane.showInputDialog(this,"Enter Parent Name for meeting: ");
        meetingDate = JOptionPane.showInputDialog(this,"Enter Date for meeting: ");
        meeting newMeeting = new meeting(meetingTeacher,meetingStudent,meetingParent,meetingDate);
        this.meetings.add(newMeeting);
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(this.meetings.size() == 0){
            jTextArea1.setText("No Meetings Arranged!");
        }
        else{
            jTextArea1.setText("Meeting Teacher Name\tMeeting Student\tMeeting Parent\t\tDate\n");
            for(int i=0;i<this.meetings.size();i++){
                this.jTextArea1.append(this.meetings.get(i).getMeetingTeacher() + "\t\t " + 
                        this.meetings.get(i).getMeetingStudent() + "\t\t" + this.meetings.get(i).getMeetingParent() + "\t\t" +
                        this.meetings.get(i).getDate() + "\n");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


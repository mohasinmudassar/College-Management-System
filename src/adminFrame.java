class meeting{
    String meetingTeacher,meetingStudent,meetingParent,date;

    public meeting(String meetingTeacher, String meetingStudent, String meetingParent,String date) {
        this.meetingTeacher = meetingTeacher;
        this.meetingStudent = meetingStudent;
        this.meetingParent = meetingParent;
        this.date = date;
        
    }

    public String getDate() {
        return date;
    }

    public void setMeetingTeacher(String meetingTeacher) {
        this.meetingTeacher = meetingTeacher;
    }

    public void setMeetingStudent(String meetingStudent) {
        this.meetingStudent = meetingStudent;
    }

    public void setMeetingParent(String meetingParent) {
        this.meetingParent = meetingParent;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMeetingTeacher() {
        return meetingTeacher;
    }

    public String getMeetingStudent() {
        return meetingStudent;
    }

    public String getMeetingParent() {
        return meetingParent;
    }
    
}

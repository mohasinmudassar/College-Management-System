    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        ArrayList<student> students = thisAdmin.displayAllStudents();
        if(students.size()==0){
            jTextArea1.setText("No Students Enrolled Yet!");
        }
        else{
            jTextArea1.setText("Name\tAge\tRollNumber\tFee Status\n");
            for(int i=0;i<students.size();i++){
                jTextArea1.append(students.get(i).getName() + "\t" + students.get(i).getAge() + "\t" + 
                        students.get(i).getRollNumber() + "\t" + students.get(i).isFeeStatus() + "\n");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

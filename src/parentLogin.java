    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int c=0;
        for(int i=0;i<this.parents.size();i++){
            //            System.out.println(parents.size());
            if(this.parentUserName.getText().equals(this.parents.get(i).getName())){
                c++;
                if(this.parentPassword.getText().equals(this.parents.get(i).getPassword())){
                    JOptionPane.showMessageDialog(this,"Login Successfull!");
                    parentFrame sf = new parentFrame();
                    sf.setParent(this.parents.get(i));
                    this.dispose();
                    sf.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this,"Invalid Password!");
                    clear();
                }
            }
        }
        if(c==0){
            JOptionPane.showMessageDialog(this,"Invalid Username and Password!");
            clear();
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    void clear(){
        this.parentUserName.setText("");
        this.parentPassword.setText("");
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = JOptionPane.showInputDialog("Enter name: ");
        String pass = JOptionPane.showInputDialog("Enter Password: ");
        int c=0;
        int age =0;
        try{
            age = Integer.parseInt(JOptionPane.showInputDialog("Enter Age: "));
        }catch(Exception e){
            c++;
        }
        if(name.equals("") || pass.equals("")){
            c++;
        }
        if(c==0){
            parent newParent = new parent();
            newParent.setName(name);
            newParent.setPassword(pass);
            newParent.setAge(age);
            this.parents.add(newParent);
            this.writeParentData();
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Invalid Entries!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed


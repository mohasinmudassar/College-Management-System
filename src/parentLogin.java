    void readParentData(){
        try  {  
            //the file to be opened for reading
            FileInputStream fis=new FileInputStream("parents.txt");       
            Scanner sc=new Scanner(fis);  
            while(sc.hasNextLine()){  
                  String data = sc.nextLine();
                  String[] adminTemp = data.split(",");
                  parent p = new parent();
                  p.setName(adminTemp[0]);
                  p.setAge(Integer.parseInt(adminTemp[1]));
                  p.setPassword(adminTemp[2]);
                  parents.add(p);     
            }  
            sc.close();     
            }catch(IOException e){  
                e.printStackTrace();  
            }
    }
    void writeParentData(){
        try{
            FileWriter myWriter = new FileWriter("parents.txt");
//            Writing all the data inside the parents Arryalist to the file line by line
            for(int i=0;i<parents.size();i++){
                myWriter.write(parents.get(i).getName()+"," + parents.get(i).getAge() + "," + parents.get(i).getPassword() + "\n");
            }
            myWriter.close();
            //System.out.println("Successfully wrote to the file.");
        }catch (IOException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }

